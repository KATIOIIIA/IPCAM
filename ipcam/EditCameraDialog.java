    /**
     * Загружаем данные
     */
    private void setCameraConfig() {
        txtNameCam.setText(camera.getNameCam());
        txtUser.setText(camera.getUserName());
        txtPassword.setText(camera.getPasswordCam());
        txtUrl.setText(camera.getUrlCam().getHost());
        txtPort.setText(String.valueOf(camera.getUrlCam().getPort()));
        txtCompanyNameCamera.setText(camera.getCompanyNameCamera());
    }

    /**
     * Очищение полей
     */
