    @SuppressWarnings("IncompatibleEquals")
    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        if (!txtNameCam.equals(camera.getNameCam())) camera.setNameCam(txtNameCam.getText());
        if (!txtUser.equals(camera.getUserName())) camera.setUserName(txtUser.getText());
        if (!txtPassword.equals(camera.getPasswordCam())) camera.setPassword(txtPassword.getText());
        if (!txtPort.equals(camera.getUrlCam().getPort()) || !txtUrl.equals(camera.getUrlCam().getHost()) ) {
            try {
                URL url = new URL("http://" + txtUrl.getText() + ":" + txtPort.getText() + "/" + camera.getAddIP());
                camera.setUrl(url);
            }catch (MalformedURLException ex) {
                Logger.getLogger(EditCameraDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        owner.updateListCameras();
        setVisible(false);
    }//GEN-LAST:event_btnOkActionPerformed

